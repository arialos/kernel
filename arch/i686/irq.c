#include "irq.h"
#include "libio.h"

// Remap the PIC to prepare for the new interrupt handlers
void irqInit()
{
    outb(0x20, 0x11);
    outb(0xA0, 0x11);
    outb(0x21, 0x20);
    outb(0xA1, 0x28);
    outb(0x21, 0x04);
    outb(0xA1, 0x02);
    outb(0x21, 0x01);
    outb(0xA1, 0x01);
    outb(0x21, 0x00);
    outb(0xA1, 0xff);
}

void irqEnable()
{
    asm volatile("sti");
}

void irqDisable()
{
    outb(0xA1, 0xff);
    outb(0x21, 0xff);
    asm volatile("cli");
}
